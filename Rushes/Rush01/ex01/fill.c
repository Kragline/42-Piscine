void	fill_matrix(int grid[9][9])
{
	int	i;
	int	j;

	i = 0;
	while (i < 9)
	{
		j = 0;
		while (j < 9)
		{
			grid[i][j] = 0;
			j++;
		}
		i++;
	}
}

void	fill_array(int array[9])
{
	int	i;

	i = 0;
	while (i < 9)
	{
		array[i] = 0;
		i++;
	}
}
